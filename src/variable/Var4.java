package variable;

public class Var4 {
    public static void main(String[] args) {
        int a = 100;
        double b = 1.5;
        boolean c = true; // false
        char d = 'A'; // 따옴표에 문자 하나만 사용
        String e = "Hello Java";

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
}
